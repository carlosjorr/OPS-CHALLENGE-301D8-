# Assigning a list of ten string elements to a variable
my_list = ["apple", "banana", "cherry", "date", "elderberry", "fig", "grape", "honeydew", "imbe", "jackfruit"]

# Printing the fourth element of the list
print("Fourth element:", my_list[3])

# Printing the sixth through tenth element of the list
print("Sixth to tenth elements:", my_list[5:])

# Changing the value of the seventh element to "onion"
my_list[6] = "onion"

# Printing the updated list
print("Updated list:", my_list)
